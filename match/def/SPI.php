      <H2><CENTER><B><font color="blue">O</font></B>bjectifs de <B><font color="blue">D</font></B>&eacute;veloppement <B><font color="blue">D</font></B>urable, 
	  la démarche du Social Progress Index<BR>&nbsp;
	  <BR></CENTER></H2>
<font face="3">
Le Social Progress Index est un indice synthétique créé dans le but de montrer le développement humain d'un pays. 
L'indice a été en cours de création entre 2009 et 2013. L'indice inclut ainsi 52 indicateurs. Il est publié par la Social Progress Imperative.
<BR><center>
<img src="./img/socialprogressindex.png" width="100%" height="100%"></center>
<BR><BR>
<B>Utilisation</B><BR>
La Commission Européenne indique souhaiter l'utiliser pour aider à répartir 63,4 milliards d'euros de fonds. Le Paraguay l'a intégré à un plan de développement. Il a également été mis en place dans 772 municipalités au Brésil, ainsi que dans l'État du Michigan, à Rio de Janeiro et à Bogota1.
<BR><BR>
<B>Composition</B><BR>
L'indice est basé sur trois axes incluant 52 indicateurs :
<ul>
<li>Les besoins de base (Basic Human Needs), basés sur l'alimentation, la santé, l'assainissement, le logement, l'accès à l'électricité, la sécurité...
</li><li>Les fondations au bien-être (Foundations of Well-being), basées sur l'alphabétisation, l'éducation, l'accès aux médias, l'espérance de vie, le taux de suicide, l'obésité, la pollution, l'environnement, etc.
</li><li>Les opportunités (Opportunity), basées sur les droits politiques, de propriété, la corruption, la tolérance sociale, l'accès à l'éducation supérieure, etc2.
</li>
</ul>
