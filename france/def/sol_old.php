On parle d’artificialisation du sol ou d'un milieu, d'un habitat naturel ou semi-naturel quand ces derniers perdent 
les qualités qui sont celles d'un milieu naturel (« naturalité », qualité qui inclut une capacité autoentretenue à abriter une certaine biodiversité, 
des cycles naturels
(cycle du carbone, cycle de l'azote, cycle de l'eau, cycle de l'oxygène, etc.) et biogéochimique (Puits de carbone par exemple).
