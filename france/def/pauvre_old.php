<H2>Taux de pauvreté selon l'&acirc;ge du r&eacute;f&eacute;rent fiscal en 2009 par d&eacute;partement</H2>
<BR><BR>
<center><img src="./img/pixabay/homeless.png" width="200" height="150" align="center"></center>
<BR>
Le taux de pauvreté correspond à la proportion d'individus (ou de ménages) dont le niveau de vie est inférieur pour une année donnée à un seuil, dénommé seuil de pauvreté (exprimé en euros).
En France, ce seuil est considéré par l'INSEE comme égal à 60% du revenu médian.
