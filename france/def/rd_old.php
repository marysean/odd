<H2>Effort de recherche et développement (%) par région en 2009</H2>
<BR><BR><center><img src="./img/pixabay/chercheur.png" width="200" height="150" align="center"></center>
<BR>
La dépense intérieure de recherche et développement correspond aux travaux de recherche et développement
exécutés sur le territoire étudié quelle que soit l'origine des fonds. 
Une partie est exécutée par les administrations, l'autre par les entreprises. 
Elle comprend les dépenses courantes (masse salariale des personnels de R&D et dépenses de fonctionnement) et les dépenses en capital 
(achats d'équipements nécessaires à la réalisation des travaux internes à la R&D et opérations immobilières réalisées dans l'année).
