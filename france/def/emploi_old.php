<H2>Taux d'emploi (%) en 2008 par d&eacute;partement</H2>
<BR><BR><center><img src="./img/pixabay/chantier.png" width="200" height="150" align="center"></center>
<BR>
Le taux d'emploi d'une classe d'individus est calculé en rapportant le nombre d'individus de la classe ayant un emploi 
au nombre total d'individus dans la classe. Il peut être calculé sur l'ensemble de la population d'un pays, 
mais on se limite le plus souvent à la population en âge de travailler 
(généralement définie, en comparaison internationale, comme les personnes âgées de 15 à 64 ans).
